    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
            switch (newAnt.changeStatus()) {
                case 0:
                    gameOver();
                    break;
                case 1:
                    progressBar();
                    newAnt.walk(evt.getKeyCode());
                    break;
                case 2:
                    progressBar();
                    //newAnt.walk(evt.getKeyCode());
                    this.newAnt.limit();
                    newAnt.hip(evt.getKeyCode());
                    break;
                case 3:
                    progressBar();
                    this.newAnt.limit();
                    newAnt.walk(evt.getKeyCode());
                    break;
                default:
                    break;
            }
    }//GEN-LAST:event_formKeyPressed
